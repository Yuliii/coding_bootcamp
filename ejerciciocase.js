let numero =  Number(prompt('Ingrese el numero:'));

switch (numero) { // switch (10)
    case  1:
        console.log('lunes');
        break;
    
    case 2:
        console.log('Martes');
        break;

    case 3:
        console.log('Miercoles');
        break;

    case 4:
        console.log('jueves');
        break;

    case 5:
        console.log('viernes');
        break;

    case 6:
        console.log('sabado');
        break;

    case 7:
        console.log('domingo');
        break;

     default:
        console.log('cargar de nuevo');
        break;

    
}